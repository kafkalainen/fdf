#include "g42.h"

void	g42_scale_point(t_vec3 *vec, double scale)
{
	t_m4x4	m;

	m.m[0][0] = scale;
	m.m[0][1] = 0;
	m.m[0][2] = 0;
	m.m[0][3] = 0;
	m.m[1][0] = 0;
	m.m[1][1] = scale;
	m.m[1][2] = 0;
	m.m[1][3] = 0;
	m.m[2][0] = 0;
	m.m[2][1] = 0;
	m.m[2][2] = scale;
	m.m[2][3] = 0;
	m.m[3][0] = 0;
	m.m[3][1] = 0;
	m.m[3][2] = 0;
	m.m[3][3] = 1;
	*vec = g42_multi_vec_matrix(vec, &m);
}
